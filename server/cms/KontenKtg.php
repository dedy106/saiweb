    function generateId($tabel, $id_tabel, $id_depan){
        $schema = db_Connect();
        $query = $schema->SelectLimit("SELECT * FROM $tabel ORDER BY $id_tabel DESC", 1);

        //apabila sudah ada data terisi, maka id akan menjadi id terbaru(terbawah), contoh SPBB-01
        if($query->RecordCount() > 0){
            $data = $query->GetRowAssoc();
            $id = $data[$id_tabel];
            $pot = explode("-", $id);

            if(isset($pot[1])){
                //angka ditambah dan diformat ulang
                $angka = $pot[1]+1;
                $angka = str_pad($angka, 4, 0, STR_PAD_LEFT);
                $id = $id_depan . '-' . $angka;
            }else{
                $id = $id+1;
            }
        }else{
            //untuk data pertama kali
            $angka = str_pad(1, 4, 0, STR_PAD_LEFT);
            $id = $id_depan . '-' . $angka;
        }

        return $id;
    }

